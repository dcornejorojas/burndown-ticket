package main

import (
	"net/http"
	"net/http/httptest"
	"testing"
	"ticket/api/routers"

	"github.com/gorilla/mux"
	"github.com/stretchr/testify/assert"
)

func Router() *mux.Router {
	router := mux.NewRouter()
	router.HandleFunc("/", routers.RootEndpoint).Methods("GET")
	return router
}

